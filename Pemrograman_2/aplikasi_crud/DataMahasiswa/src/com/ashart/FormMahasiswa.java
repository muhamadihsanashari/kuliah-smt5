    private void load_table() {
        //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("NIM");
        model.addColumn("Jurusan");
        model.addColumn("Alamat");
        model.addColumn("No. Telp");
        
        //menampilkan data database ke dalam tabel
        try {
            int no=1;
            String sql = "select * from mahasiswa";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
            tblMahasiswa.setModel(model);
        } catch (Exception e){
        }
    }

    private void kosong(){
        edtTextNama.setText(null);
        edtTextNim.setText(null);
        edtTextAlamat.setText(null);
        edtTextNoTelp.setText(null);
        CbJurusan.setSelectedItem(null);
    }
