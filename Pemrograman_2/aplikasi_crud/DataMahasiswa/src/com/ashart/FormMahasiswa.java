            String sql ="UPDATE mahasiswa SET nama = '"+edtTextNama.getText()+"', nim = '"+edtTextNim.getText()+"', jurusan = '"+CbJurusan.getSelectedItem()+"',alamat= '"+edtTextAlamat.getText()+"',phone= '"+edtTextNoTelp.getText()+"' WHERE nim = '"+edtTextNim.getText()+"'";
